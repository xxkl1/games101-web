/**
 * 测试水平线
 */
const testDrawLine1 = function () {
    const imageData = new ImageData(5, 1);
    drawLine(
        imageData,
        { x: 1, y: 1 },
        { x: 5, y: 1 },
        { r: 255, g: 0, b: 0, a: 255 }
    );

    ensure(
        Array.from(imageData.data),
        [
            255, 0, 0, 255, 255, 0, 0, 255, 255, 0, 0, 255, 255, 0, 0, 255, 255,
            0, 0, 255,
        ],
        "testDrawLine1"
    );
};

